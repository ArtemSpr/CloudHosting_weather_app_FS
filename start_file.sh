#!/bin/bash

# Перевірка наявності повідомлення коміту
if [ -z "$1" ]; then
  echo "Будь ласка, введіть повідомлення коміту після /start_file"
  exit 1
fi

COMMIT_MESSAGE="$*"

echo "=============================="
echo "Git автоматизація стартує..."
echo "Повідомлення коміту: $COMMIT_MESSAGE"
echo "=============================="

echo "Додаємо файли..."
git add .
sleep 1  # пауза для візуального ефекту

echo "Створюємо коміт..."
git commit -m "$COMMIT_MESSAGE"
sleep 1

echo "Надсилаємо на віддалений репозиторій..."
git push
sleep 1

echo "=============================="
echo "Готово! Коміт '$COMMIT_MESSAGE' успішно виконано."
echo "=============================="
